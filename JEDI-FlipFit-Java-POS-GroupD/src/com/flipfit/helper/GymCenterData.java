package com.flipfit.helper;

public class GymCenterData {
    private String gymName;
    private String gymAddress;
    private String gymGSTIN;
    private int equipments;

    public String getGymName() {
        return gymName;
    }

    public void setGymName(String gymName) {
        this.gymName = gymName;
    }

    public String getGymAddress() {
        return gymAddress;
    }

    public void setGymAddress(String gymAddress) {
        this.gymAddress = gymAddress;
    }

    public String getGymGSTIN() {
        return gymGSTIN;
    }

    public void setGymGSTIN(String gymGSTIN) {
        this.gymGSTIN = gymGSTIN;
    }

    public int getEquipments() {
        return equipments;
    }

    public void setEquipments(int equipments) {
        this.equipments = equipments;
    }
}
